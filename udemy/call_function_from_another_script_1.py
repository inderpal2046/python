#!/usr/bin/env python3

def add(a,b):
    print(f"Sum of {a} and {b} is {a+b}")
    return None

def subtract(a,b):
    print(f"Difference between {a} and {b} is {a-b}")
    return None

def main():
    x=8
    y=2
    add(x,y)
    subtract(x,y)
    return None

if __name__=="__main__":
    main() 
