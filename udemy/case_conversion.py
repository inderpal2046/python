#!/usr/bin/env python3

name="Inderpal Singh Saini"
print(f"Original string is: {name} \n")
print(f"length of string is: {len(name)} \n")
lowercase=name.lower()
print(f"Lowercase string is: {lowercase} \n")
print(f"Uppercase string is: {name.upper()} \n")
print(f"Below operations can be performed on our string, \n")
print(f"{dir(name)} \n")
print(f"The string in title format is: {name.title()} \n")
print(f"The string in capitalized format is: {name.capitalize()} \n")
print(f"The strign in swapped case format is: {name.swapcase()} \n")
